def drawChar(x: int, y: int, c: int, size: int):
  """Draw a character to the screen at a given position and scale.
  Port of the C VGA driver function.

  Parameters
  ----------
  x : int (short)
      screen x-coordinate
  y : int (short)
      screen y-coordinate
  c : int (color)
      Co
  size : int (uchar)
      Size of a pixel
      
  Notes
  -----
  Does not have a color parameter (just black with transparent background).
  """
  if ((x >= width) or
      (y >= height) or
      ((x + 6 * size - 1) < 0) or
      ((y + 8 * size - 1) < 0)):
      return
  # This is the main reason I'm using pygame
  # it's super easy to mirror the graphics-code using a PixelArray
  vga_data_array = pygame.PixelArray(screen)
  for i in range(px_x):
    line: int
    if (i == 5):
      line = 0x0
      # Modification from original driver
      if (c >= 0xae and c <= 0xde):
        break
    else:
      line = raw_font[(c*5)+i]
    for j in range(px_y):
      if (line & 0x1):
        if (size == 1):
          xx = x + i
          yy = y + j
          # ignore the squiggles if you have them. my Pylance doesn't think this
          # is allowed
          # white pixel
          vga_data_array[x+i, y+j] = (255, 255, 255) #type: ignore
        else:
          # white square 
          vga_data_array[x+(i*size):x+(i*size)+size, y+(j*size):y+(j*size)+size] = (255, 255, 255) #type: ignore
        
      # elif (bg != color):
      #   if (size == 1):
      #     vga_data_array[x+i, y+j] = 0
      #   else:
      #     fillRect(x+i*size, y+j*size, size, size, bg);
        
      line >>= 1
      
  # have to close the PixelArray since it locks the surface it's referencing
  # (the screen in this case)
  vga_data_array.close()

screen_changed = True
while running:
  
  # Static image so only recompute on first loop/if the screen changes
  if screen_changed:
    screen_ratio = width/height
    disp_ratio = window_size[0]/window_size[1]
    disp_screen_x = window_size[0] / width
